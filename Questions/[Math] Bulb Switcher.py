# Question: https://leetcode.com/problems/bulb-switcher/
# Medium

import math

class Solution:
    # O(1) time and space
    def bulbSwitch(self, n: int) -> int:
        # only the bulbs toggled odd number of times will stay on at the end
        # only perfect squares have odd number of factors
        
        return int(math.sqrt(n))


